package com.tnsif.assessment;

public class typeConversion {

	public static void main(String[] args) {

		//Implicit type conversion
				int a=10;
				double b=a;
				System.out.println(b);
			//Explicit type conversion
			float c=10.78f;
			int s=(int)c;
			System.out.println(s);

		}

}
